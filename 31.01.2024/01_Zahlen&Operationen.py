# Demo, um Zahlen und Operationen darauf in Python zu erklären

print(5)
print (2.5)

print(5+5)  # Addition
print(5+5.0)  # Addition float
print(30-15)   # Subtraktion

print(20*18)   # Multiplikation
print(100/12)   # Division
print(100//12)   # Ganzzahldivision
print(20%6)  # Rest der Ganzzahldivision

print(8.25+4.71)  # Addition mit Kommazahlen  -> Ergebnistyp ist Float
print(12*4.3)  # Multiplikation mit Kommazahlen -> dito
print(21-4*2)  # Punkt vor Strich
print((21-4)*2) # Klammerrechnen (für die Steuerung der Reihenfolge)
