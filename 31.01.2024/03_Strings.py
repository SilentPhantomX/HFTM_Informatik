# Demo für die Verwendung von Strings

name = "Höhere Fachschule für Technik Mittelland"
print(name)

print(name[0]) # Zählen beginnt bei Null somit wird das erste zeichen ausgegeben
print(name[7])
print(name[-1]) # beginnt von hinten mit der Ausgabe

print(name[0]) # H
print(name[7]) # F
print(name[22]) # T
print(name[30]) # M

print(name[1:5])
